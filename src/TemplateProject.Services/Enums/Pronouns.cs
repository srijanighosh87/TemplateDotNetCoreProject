namespace TemplateProject.Services.Enums;

/// <summary>
/// Represents pronoun options for a user.
/// </summary>
public enum Pronouns
{
  /// <summary>
  /// Represents an unspecified pronoun option.
  /// </summary>
  Unspecified,

  /// <summary>
  /// Represents a person who identifies as a man.
  /// </summary>
  He,

  /// <summary>
  /// Represents a person identifies as a woman.
  /// </summary>
  She,

  /// <summary>
  /// Represents a person who neither represents as a man nor a woman.
  /// </summary>
  They
}
